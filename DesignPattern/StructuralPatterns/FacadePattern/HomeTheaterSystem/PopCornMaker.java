package DesignPattern.StructuralPatterns.FacadePattern.HomeTheaterSystem;

public class PopCornMaker {
    
    public void on() {
        System.out.println("Starting PopCornMaker...");
    }

    public void popPopcorn() {
        System.out.println("Popping Pop Corn...");
    }

    public void off() {
        System.out.println("Switching off pop corn maker...");
    }
}
