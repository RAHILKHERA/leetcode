    public int minFallingPathSum(int[][] matrix) {

        int n = matrix.length;

        for (int row = n - 2; row >= 0; row--) {

            for (int col = 0; col < n; col++) {
                int min = matrix[row + 1][col];

                if (col >= 1) {

                    min = min < matrix[row + 1][col - 1] ? min : matrix[row + 1][col - 1];
                }

                if (col + 1 < n) {
                    min = min < matrix[row + 1][col + 1] ? min : matrix[row + 1][col + 1];
                }

                matrix[row][col] += min;

            }
        }

        int min = matrix[0][0];

        for (int i = 1; i < n; i++) {
            min = min < matrix[0][i] ? min : matrix[0][i];
        }

        return min;
    }

